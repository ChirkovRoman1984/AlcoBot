class ReadLines:

    def __init__(self, name: str):
        self.file_name: str = name
        self.lines: list = []

    async def read_file(self) -> list:
        async with aiofiles.open(self.file_name, mode='r') as file:
            text = await file.read()
        return text.splitlines()

    @property
    async def random_string(self) -> str:
        if len(self.lines) == 0:
            self.lines = await self.read_file()

        line = random.choice(self.lines)
        self.lines.remove(line)
        return line


