    banya = random.choice(bani)
    images = banya['images']
    random.shuffle(images)
    images = images[:3]
    media = types.MediaGroup()
    text = f'Давай сауну закажешь? Смотри какую я подобрал:\n' \
           f'{banya["name"]}\n{banya["adress"]}\n{banya["price"]}\n{banya["phone"]}'
    await message.answer(text=text)
    for image in images:
        media.attach_photo(image)
    await bot.send_media_group(chat_id=message.chat.id, media=media)


